void	ft_sort_int_tab(int *tab, int size)

{
	int	i;
	int	tem;

	i = 0;
	while (i < size - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			tem = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = tem;
			i = -1;
		}
		i++;
	}
}
