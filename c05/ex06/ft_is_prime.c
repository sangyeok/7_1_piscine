int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb < 2 || nb == 4)
		return (0);
	if (nb == 2)
		return (1);
	while ((i < nb / 2) && i < 46341)
	{
		if ((nb % i) != 0)
			i++;
		else if ((nb % i) == 0)
			return (0);
	}
	return (1);
}
